const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
	 '**********': ' ',
};

function decode(expr) {
	 let elements = expr.match(/.{10}/g);
	 let elementsReplace = elements.map(element => element.replace(/10/g, '.').replace(/11/g, '-').replace(/00/g, ''));
	 let elementsNewValue = elementsReplace.map(element => element = MORSE_TABLE[element]);
	 let elementsNewString = elementsNewValue.join('');
	 return elementsNewString;
}

module.exports = {
    decode
}